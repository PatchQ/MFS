def tw_to_slug(name):
    """將繁體中文轉為 URL slug（與 TradingView 一致）"""
    # 替換特殊字符
    s = name.replace("：", "-").replace("/", "-")
    s = re.sub(r"[^a-zA-Z0-9\u4e00-\u9fff-]", "", s)
    s = re.sub(r"-+", "-", s).strip("-").lower()
    return s

def make_url(name):
    slug = tw_to_slug(name)
    return f"https://tw.tradingview.com/markets/stocks-hong-kong/sectorandindustry-industry/{slug}/"

